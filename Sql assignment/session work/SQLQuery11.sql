
/* ALL EMP DETAILS*/
SELECT *
FROM EMP;

/*ONLY ANALYST AND SALESMAN*/
SELECT  ENAME
FROM EMP
WHERE JOB='ANALYST'
OR JOB='SALESMAN';

/*ONLE EMPLOYEES WITH 7369 ,7521, 7839, 7934, 7788*/ 
SELECT ENAME, EMPNO
FROM EMP
WHERE EMPNO IN(7369,7521,7839,7934,7788);

/* NOT BELONG TO DEPTNO 30,40,10*/
SELECT ENAME
FROM EMP
WHERE EMPNO NOT IN(30,40,10);

/*EMPS F DEPT 20*/
SELECT ENAME
FROM EMP
WHERE DEPTNO=20;

/*NAME HIREDATE WITH SAL IN DEC*/
SELECT ENAME,HIREDATE,SAL
FROM EMP
ORDER BY SAL DESC;

/* HRA DA PF*/
SELECT ENAME,SAL,(SAL*12)/100 AS PF,(SAL*50)/100 AS HRA,(SAL*30)/100 AS DA,(SAL+(SAL*012)+(SAL*0.3)+(SAL*0.5))AS 'Gross'
FROM EMP
ORDER BY Gross;

/*NOT REPORTING TO ANYBODY*/
SELECT ENAME,JOB,lower(ENAME),upper(ENAME)
FROM EMP
WHERE JOB='PRESIDENT';



